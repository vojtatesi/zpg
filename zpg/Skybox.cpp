Skybox::Skybox(std::shared_ptr<ShaderProgram> shader, std::shared_ptr<CubeTexture> cubemap) : shader(shader), cubemap(cubemap)
{
	glGenVertexArrays(1, &VAO);
	glGenBuffers(1, &VBO);

	glBindVertexArray(VAO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);

	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

	glBindVertexArray(0);
}

void Skybox::draw(const std::shared_ptr<Camera>& camera) {
	glDepthFunc(GL_LEQUAL);

	shader->use();

	glm::mat4 view = glm::mat4(glm::mat3(camera->getViewMatrix()));

	shader->setUniformMatrix4fv("uView", view);
	shader->setUniformMatrix4fv("uProjection", camera->getProjection());

	cubemap->bind();
	shader->setUniform1i("skybox", 0);

	glBindVertexArray(VAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);

	glDepthFunc(GL_LESS);
}
